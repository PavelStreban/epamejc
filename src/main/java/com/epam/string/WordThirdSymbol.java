package com.epam.string;

public class WordThirdSymbol {

    public static void main(String[] args) {
        String word = "0123456789";
        int key = 4;
        System.out.println("Задание 9.13 - " + word.charAt(2));
        System.out.println("Задание 9.14 - " + word.charAt(word.length() - 1));
        System.out.println("Задание 9.15 - " + word.charAt(key - 1));
    }
}
